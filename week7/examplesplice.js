let num = [1,2,3,4,5]
num.splice(2,3) //ลบ 3,4,5
console.log(num); //1,2

let num2 = [1,2,3,4,5]
num2.splice(2,3,6,7) // เพิ่ม 6 กับ 7 เข้าไป (เราสามารถเพื่มเลขต่อท้ายไปเรื่อยๆได้)
console.log(num2); //1,2,6,7

let num3 = [1,2,3,4,5]
num3.splice(2,0,6,7) // เอา 3,4,5 ถอยออกไปแล้วแทรก 6,7 เข้ามา
console.log(num3); //1,2,6,7,3,4,5
